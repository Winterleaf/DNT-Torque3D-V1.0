        /// <summary>
        ///   Checks the string to ensure it is a valid numeric string.
        /// </summary>
        /// <param name="numberString"> </param>
        /// <returns> </returns>
        public static bool IsNumeric(this string numberString)
            {
            foreach (byte c in numberString.ToArray())
                {
                if ((((c < 48) || (c > 57)) && (c != 46) && (c != 44)))
                    return false;
                }
            return true;
            }
